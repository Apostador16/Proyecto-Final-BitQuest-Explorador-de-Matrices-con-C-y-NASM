int main() {
    int nivel_actual = 0;
    Jugador jugador = {0, 0, 0, 0, 0};
    
    int jugando = 1;
    int niveles_completados = 0;
    int total_monedas_juego = 0;
    int recolectadas_juego = 0;
    int pasos_juego = 0;

    while (nivel_actual < NUM_NIVELES && jugando) {
        jugador.monedas_recolectadas = 0;
        jugador.tiene_llave = 0;
        jugador.pasos = 0;

        // Buscar posicion inicial P
        for (int i = 0; i < FILAS_MAPA; i++) {
            for (int j = 0; j < COLUMNAS_MAPA; j++) {
                if (mapas[nivel_actual][i][j] == 'P') {
                    jugador.fila = i;
                    jugador.col = j;
                    break;
                }
            }
        }

        // NASM: Contar monedas totales del nivel
        int total_celdas = FILAS_MAPA * (COLUMNAS_MAPA + 1);
        int total_monedas_nivel = contar_caracteres(&mapas[nivel_actual][0][0], total_celdas, 'M');
        total_monedas_juego += total_monedas_nivel;

        int nivel_completado = 0;

        // Bucle principal del nivel
        while (!nivel_completado && jugando) {
            system("cls"); 
            imprimir_ventana(mapas[nivel_actual], jugador);
            
            printf("\nNivel: %d | Monedas: %d/%d | Llave: %s | Pasos: %d\n", 
                   nivel_actual + 1, jugador.monedas_recolectadas, total_monedas_nivel, 
                   jugador.tiene_llave ? "Si" : "No", jugador.pasos);
            printf("Controles: W (Arriba) S (Abajo) A (Izq) D (Der) | Q (Salir)\n");
            
            char tecla = _getch(); 

            int nueva_fila = jugador.fila;
            int nueva_col = jugador.col;

            if (tecla == 'w' || tecla == 'W') nueva_fila--;
            else if (tecla == 's' || tecla == 'S') nueva_fila++;
            else if (tecla == 'a' || tecla == 'A') nueva_col--;
            else if (tecla == 'd' || tecla == 'D') nueva_col++;
            else if (tecla == 'q' || tecla == 'Q') { jugando = 0; break; }

            // NASM: Validar movimiento
            if (validar_movimiento(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col) == 1) {
                
                // NASM: Verificar puerta
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col, 'D') == 1) {
                    if (jugador.tiene_llave == 0) continue;
                }
                
                // NASM: Verificar salida
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col, 'E') == 1) {
                    nivel_completado = 1;
                    continue;
                }

                // NASM: Verificar llave
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col, 'K') == 1) {
                    jugador.tiene_llave = 1;
                }

                // NASM: Verificar moneda
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col, 'M') == 1) {
                    jugador.monedas_recolectadas++;
                }

                // Actualizar posiciones en la matriz
                mapas[nivel_actual][jugador.fila][jugador.col] = '.';
                jugador.fila = nueva_fila;
                jugador.col = nueva_col;
                jugador.pasos++;
                mapas[nivel_actual][jugador.fila][jugador.col] = 'P';
            }
        }

        // Resumen fin de nivel
        if (nivel_completado) {
            niveles_completados++;
            recolectadas_juego += jugador.monedas_recolectadas;
            pasos_juego += jugador.pasos;
            
            // NASM: Contar celdas libres
            int celdas_libres = contar_celdas_libres(&mapas[nivel_actual][0][0], total_celdas);

            system("cls");
            printf("=================================\n");
            printf("       Nivel %d completado\n", nivel_actual + 1);
            printf(" Monedas recolectadas: %d/%d\n", jugador.monedas_recolectadas, total_monedas_nivel);
            printf(" Pasos realizados: %d\n", jugador.pasos);
            printf(" Celdas libres en el mapa: %d\n", celdas_libres);
            printf("=================================\n");
            system("pause");
            
            nivel_actual++;
        }
    }

    // Resumen fin de juego
    if (niveles_completados == NUM_NIVELES) {
        
        // NASM: Calcular puntaje
        int puntaje_final = calcular_puntaje(recolectadas_juego, pasos_juego, niveles_completados);

        system("cls");
        printf("=================================\n");
        printf("       Juego completado\n");
        printf("=================================\n");
        printf(" Monedas totales: %d/%d\n", recolectadas_juego, total_monedas_juego);
        printf(" Pasos totales: %d\n", pasos_juego);
        printf(" Niveles completados: %d\n", niveles_completados);
        printf(" Puntaje final: %d\n", puntaje_final);
        printf("=================================\n");
        system("pause");
    } else {
        system("cls");
        printf("Saliste del juego. Hasta pronto!\n");
    }

    return 0;
}
