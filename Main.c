void mostrar_menu(int *dificultad, int *nivel_inicio) {
    int opcion_valida = 0;
    while (!opcion_valida) {
        system("cls");
        printf("=== MENU PRINCIPAL ===\n\n");
        printf("Selecciona la dificultad:\n");
        printf("1. Modo Facil   (Sin limite de pasos)\n");
        printf("2. Modo Normal  (Con limite de pasos)\n");
        printf("3. Modo Dificil (Pocos pasos)\n");
        printf("Elige una opcion (1-3): ");
        char opc1 = _getch();
        if (opc1 >= '1' && opc1 <= '3') {
            *dificultad = opc1 - '0';
            opcion_valida = 1;
        }
    }

    opcion_valida = 0;
    while (!opcion_valida) {
        system("cls");
        printf("=== SELECCION DE NIVEL ===\n\n");
        printf("Selecciona el laberinto inicial (1 al 3):\n");
        printf("1. Nivel 1\n");
        printf("2. Nivel 2\n");
        printf("3. Nivel 3\n");
        printf("Elige una opcion (1-3): ");
        char opc2 = _getch();
        if (opc2 >= '1' && opc2 <= '3') {
            *nivel_inicio = (opc2 - '0') - 1; 
            opcion_valida = 1;
        }
    }
}

int main() {
    int dificultad = 1;
    int nivel_inicio = 0;
    
    // Mostramos el menu antes de arrancar
    mostrar_menu(&dificultad, &nivel_inicio);

    // Definicion de limites de pasos reducidos a 3 niveles
    int limites_normal[3] = {120, 250, 350};
    int limites_dificil[3] = {80, 150, 200};

    int nivel_actual = nivel_inicio;
    int total_monedas_juego = 0;
    int recolectadas_juego = 0;
    int pasos_juego = 0;
    int jugando = 1;

    // Calcular primero el total global de monedas usando NASM
    int total_celdas = FILAS_MAPA * (COLUMNAS_MAPA + 1);
    for (int n = 0; n < NUM_NIVELES; n++) {
        total_monedas_juego += contar_caracteres(&mapas[n][0][0], total_celdas, 'M');
    }

    while (nivel_actual < NUM_NIVELES && jugando) {
        Jugador jugador = {0, 0, 0, 0, 0};
        int nivel_completado = 0;
        int game_over = 0;

        // Ajustar el limite de pasos dependiendo de la dificultad elegida
        int limite_actual = 0;
        if (dificultad == 2) limite_actual = limites_normal[nivel_actual];
        else if (dificultad == 3) limite_actual = limites_dificil[nivel_actual];

        // Buscar al jugador en la matriz
        for (int i = 0; i < FILAS_MAPA; i++) {
            for (int j = 0; j < COLUMNAS_MAPA; j++) {
                if (mapas[nivel_actual][i][j] == 'P') {
                    jugador.fila = i;
                    jugador.col = j;
                    break;
                }
            }
        }

        // Monedas que tiene este nivel específico
        int total_monedas_nivel = contar_caracteres(&mapas[nivel_actual][0][0], total_celdas, 'M');

        // Bucle principal de juego
        while (!nivel_completado && !game_over && jugando) {
            system("cls");
            
            // Dibujar la ventana 20x20
            imprimir_ventana(mapas[nivel_actual], jugador);

            // Interfaz de estado inferior
            printf("\nNivel: %d | Pasos: %d", nivel_actual + 1, jugador.pasos);
            if (limite_actual > 0) printf("/%d", limite_actual);
            printf(" | Monedas: %d/%d | Llave: %s\n", 
                   jugador.monedas_recolectadas, total_monedas_nivel, 
                   jugador.tiene_llave ? "Si" : "No");
            printf("Controles: W/A/S/D - Q para salir\n");

            char tecla = _getch();
            int nueva_fila = jugador.fila;
            int nueva_col = jugador.col;

            if (tecla == 'w' || tecla == 'W') nueva_fila--;
            else if (tecla == 's' || tecla == 'S') nueva_fila++;
            else if (tecla == 'a' || tecla == 'A') nueva_col--;
            else if (tecla == 'd' || tecla == 'D') nueva_col++;
            else if (tecla == 'q' || tecla == 'Q') jugando = 0;

            // Enviar COLUMNAS_MAPA + 1 (61) para NASM
            int puede_caminar = validar_movimiento(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col);
            int es_puerta = detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, nueva_fila, nueva_col, 'D');

            // Regla de movimiento fisica
            if (puede_caminar && (!es_puerta || (es_puerta && jugador.tiene_llave))) {
                mapas[nivel_actual][jugador.fila][jugador.col] = '.'; // Limpiar rastro viejo
                
                jugador.fila = nueva_fila;
                jugador.col = nueva_col;
                jugador.pasos++;

                // Escaneo de interacciones NASM
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, jugador.fila, jugador.col, 'M')) {
                    jugador.monedas_recolectadas++;
                }
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, jugador.fila, jugador.col, 'K')) {
                    jugador.tiene_llave = 1;
                }
                if (detectar_objeto(&mapas[nivel_actual][0][0], COLUMNAS_MAPA + 1, jugador.fila, jugador.col, 'E')) {
                    nivel_completado = 1;
                }

                // Verificar Game Over
                if (limite_actual > 0 && jugador.pasos >= limite_actual && !nivel_completado) {
                    game_over = 1;
                }

                // Pintar la 'P'
                if (!nivel_completado && !game_over) {
                    mapas[nivel_actual][jugador.fila][jugador.col] = 'P';
                }
            }
        }

        // Fin de nivel
        if (game_over) {
            system("cls");
            printf("=================================\n");
            printf("           GAME OVER\n");
            printf("=================================\n");
            printf(" Te has quedado sin pasos.\n");
            system("pause");
            jugando = 0; 
        } else if (nivel_completado) {
            recolectadas_juego += jugador.monedas_recolectadas;
            pasos_juego += jugador.pasos;

            int celdas_libres = contar_celdas_libres(&mapas[nivel_actual][0][0], total_celdas);

            system("cls");
            printf("=================================\n");
            printf("       Nivel %d completado\n", nivel_actual + 1);
            printf(" Monedas recolectadas: %d/%d\n", jugador.monedas_recolectadas, total_monedas_nivel);
            printf(" Pasos realizados: %d\n", jugador.pasos);
            printf(" Celdas libres en el mapa: %d\n", celdas_libres);
            printf("=================================\n");
            system("pause");

            nivel_actual++;
        }
    }

    // Pantalla de Estadísticas Totales (Victoria)
    if (nivel_actual >= NUM_NIVELES && jugando) {
        int puntaje_final = calcular_puntaje(recolectadas_juego, pasos_juego, NUM_NIVELES);

        system("cls");
        printf("=================================\n");
        printf("       Juego Completado\n");
        printf("=================================\n");
        printf(" Monedas totales: %d/%d\n", recolectadas_juego, total_monedas_juego);
        printf(" Pasos totales: %d\n", pasos_juego);
        printf(" PUNTAJE FINAL: %d\n", puntaje_final);
        printf("=================================\n");
        system("pause");
    }

    return 0;
}
